package color.dto;

public class ColorDTO {
   private int p_num;
   private int col_num;
   private String col_name;
   private String col_size;
   private int col_stock;
   public int getP_num() {
      return p_num;
   }
   public void setP_num(int p_num) {
      this.p_num = p_num;
   }
   public int getCol_num() {
      return col_num;
   }
   public void setCol_num(int col_num) {
      this.col_num = col_num;
   }
   public String getCol_name() {
      return col_name;
   }
   public void setCol_name(String col_name) {
      this.col_name = col_name;
   }
   public String getCol_size() {
      return col_size;
   }
   public void setCol_size(String col_size) {
      this.col_size = col_size;
   }
   public int getCol_stock() {
      return col_stock;
   }
   public void setCol_stock(int col_stock) {
      this.col_stock = col_stock;
   }
   
}